#JLW: When passed the vector of functions generated by makeCacheMatrix, returnes
# the inverse of the matrix from cache if available or solves it and stores it
# in cache

cacheSolve <- function(x, ...) {
        ## Return a matrix that is the inverse of 'x'
  m <- x$getInverse()
  if(!is.null(m)){
    message("getting cached data")
    return(m)
  }
  data <-  x$get()
  m <- solve(data,...)
  x$setInverse(m)
  m
}
