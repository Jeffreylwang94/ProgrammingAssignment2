#JLW: makeCacheMatrix returns. a list of functions that can store a matrix in a 
# cache as well as it's inverse on calculation with solve

makeCacheMatrix <- function(x = matrix()) {
  m <- NULL
  set <- function(y) {
      x <<- y
      m <<- NULL
  }
  get <- function() x
  setInverse <- function(solve) m <<- solve
  getInverse <-  function() m
  list(set = set, get = get, setInverse=setInverse, getInverse=getInverse)

}


